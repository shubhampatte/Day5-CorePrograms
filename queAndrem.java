public class queAndrem
{
	public static void main(String args[])
	{
		int dividend = 25;
		int divisior = 2;
		
		System.out.println("Dividend is : " +dividend+ " and Divisior is : " +divisior);
			
		float quotient = (float) dividend / divisior;
		int remainder = dividend % divisior;
			
		System.out.println("Quotient is : " +quotient+ " and Remainder is : " +remainder);	
		
	}	

}
